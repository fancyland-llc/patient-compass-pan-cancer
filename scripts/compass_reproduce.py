def fetch_api(url, data=None, retries=3):
    """Fetch JSON from cBioPortal API."""
    from urllib.request import Request, urlopen
    for attempt in range(retries):
        try:
            body = json.dumps(data).encode('utf-8') if data else None
            method = 'POST' if body else 'GET'
            req = Request(url, data=body, method=method)
            req.add_header('Accept', 'application/json')
            if body:
                req.add_header('Content-Type', 'application/json')
            with urlopen(req, timeout=180) as resp:
                return json.loads(resp.read().decode())
        except Exception as e:
            if attempt == retries - 1:
                raise
            time.sleep(3 * (attempt + 1))


def count_complete_pairs(h):
    """Count how many of the 4 hallmark pairs are fully complete (both active)."""
    return sum(1 for a, b in PAIRS if h[a] and h[b])


def count_domain_walls(h):
    """Count broken pairs (0–4). A broken pair = exactly one hallmark of a pair is active."""
    return sum(1 for a, b in PAIRS if bool(h[a]) != bool(h[b]))


def compute_wall_features(h):
    """Compute the 10 domain wall features from an 8-bit hallmark vector."""
    walls = {'Genome': int(h[6])}
    for h_a, h_b, coupled, only_a, only_b in WALL_PAIRS:
        if h[h_a] and h[h_b]:
            walls[coupled] = 1; walls[only_a] = 0; walls[only_b] = 0
        elif h[h_a] and not h[h_b]:
            walls[coupled] = 0; walls[only_a] = 1; walls[only_b] = 0
        elif not h[h_a] and h[h_b]:
            walls[coupled] = 0; walls[only_a] = 0; walls[only_b] = 1
        else:
            walls[coupled] = 0; walls[only_a] = 0; walls[only_b] = 0
    return walls


def wall_risk_score(h):
    """Compute the domain wall Cox risk score: sum(beta_i * wall_i).
    This is the linear predictor from the Cox PH model (standard biostatistics)."""
    walls = compute_wall_features(h)
    return sum(WALL_COEFS[k] * v for k, v in walls.items())


def mann_whitney_z(x, y):
    """Two-sample Mann-Whitney U test, returns z-score and p-value."""
    n1, n2 = len(x), len(y)
    if n1 < 3 or n2 < 3:
        return 0.0, 1.0
    combined = np.concatenate([x, y])
    ranks = np.argsort(np.argsort(combined)).astype(float) + 1
    U1 = ranks[:n1].sum() - n1 * (n1 + 1) / 2
    mu = n1 * n2 / 2
    sigma = sqrt(n1 * n2 * (n1 + n2 + 1) / 12)
    z = (U1 - mu) / sigma if sigma > 0 else 0.0
    p = erfc(abs(z) / sqrt(2))
    return z, p


def cox_fit_lifelines(X, time, event, feature_names, penalizer=0.01):
    """Fit a Cox PH model using lifelines CoxPHFitter (Efron ties, Ridge penalty).

    Returns:
        params: dict  {feature_name: coefficient}
        se:     dict  {feature_name: standard_error}
        c_index: float
        converged: bool (always True if lifelines doesn't raise)
    """
    import pandas as pd
    from lifelines import CoxPHFitter

    df = pd.DataFrame(X, columns=feature_names)
    df['T'] = time
    df['E'] = event

    cph = CoxPHFitter(penalizer=penalizer)
    cph.fit(df, duration_col='T', event_col='E')

    params = {f: cph.params_[f] for f in feature_names}
    se = {f: cph.summary.loc[f, 'se(coef)'] for f in feature_names}
    return params, se, cph.concordance_index_, True


def classify_o2(cancer_type):
    """Classify a cancer type's tissue O₂ environment."""
    ct = cancer_type.lower().replace('-', '').replace('_', '')
    for key, tier in TISSUE_O2.items():
        if key in ct:
            return tier
    return 'MEDIUM'  # default for unknown types


def hamming_neighbors(state_str):
    """Return all 8 Hamming-distance-1 neighbors of an 8-bit state string."""
    neighbors = []
    for i in range(8):
        arr = list(state_str)
        arr[i] = '0' if arr[i] == '1' else '1'
        neighbors.append(''.join(arr))
    return neighbors


