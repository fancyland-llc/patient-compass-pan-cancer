def load_from_compact(path):
    """Load patient data from the compact JSON (no patent content in this file)."""
    with open(path, 'r', encoding='utf-8') as f:
        raw = json.load(f)
    patients = []
    for p in raw:
        h = p['h'] if isinstance(p['h'], list) else [int(c) for c in p.get('sig', '00000000')]
        patients.append({
            'sig': p.get('sig', ''.join(str(x) for x in h)),
            'hallmarks': h,
            'n_h': sum(h),
            'pairs': count_complete_pairs(h),
            'n_walls': count_domain_walls(h),
            'walls': compute_wall_features(h),
            'wall_score': wall_risk_score(h),
            'os_mo': p.get('os_mo'),
            'deceased': p.get('deceased', 0),
            'has_surv': p.get('os_mo') is not None,
            'study': p.get('study', ''),
            'ctype': p.get('ctype', ''),
            'o2': classify_o2(p.get('ctype', '')),
        })
    return patients


def fetch_fresh():
    """Fetch everything from cBioPortal. Same pipeline as compass_100k.py
    but WITHOUT any eigenvalue / commutator / sigma_z computation."""
    print('  Discovering studies from cBioPortal...')
    all_studies = fetch_api(f'{BASE}/studies?projection=DETAILED&pageSize=10000000')
    print(f'    Total studies on cBioPortal: {len(all_studies)}')

    usable = []
    for study in all_studies:
        sid = study['studyId']
        if 'cellline' in sid or 'ccle' in sid:
            continue
        ct = study.get('cancerTypeId', '').lower()
        nm = study.get('name', '').lower()
        if any(h in ct or h in nm or h in sid for h in HEME_TYPES):
            continue
        try:
            profiles = fetch_api(f'{BASE}/studies/{sid}/molecular-profiles')
            mut_profs = [p for p in profiles if p.get('molecularAlterationType') == 'MUTATION_EXTENDED']
            if not mut_profs:
                continue
            samples = fetch_api(f'{BASE}/studies/{sid}/samples?projection=ID&pageSize=100000')
            if len(samples) < 50:
                continue
            usable.append({
                'study_id': sid,
                'name': study.get('name', sid),
                'n_samples': len(samples),
                'mut_profile': mut_profs[0]['molecularProfileId'],
                'cancer_type': study.get('cancerTypeId', 'unknown'),
            })
        except Exception:
            continue

    usable.sort(key=lambda s: -s['n_samples'])
    print(f'    Usable studies: {len(usable)} ({sum(s["n_samples"] for s in usable):,} potential samples)')

    patients = []
    seen = set()

    for si, info in enumerate(usable):
        sid = info['study_id']
        mut_prof = info['mut_profile']
        label = info['name'][:40]
        print(f'  [{si+1:3d}/{len(usable)}] {label:<42s}', end='', flush=True)

        try:
            samples = fetch_api(f'{BASE}/studies/{sid}/samples?pageSize=100000')
            sample_ids = [s['sampleId'] for s in samples]
            s2p = {s['sampleId']: s['patientId'] for s in samples}

            try:
                mutations = fetch_api(
                    f'{BASE}/molecular-profiles/{mut_prof}/mutations/fetch',
                    data={'sampleListId': f'{sid}_all', 'entrezGeneIds': gene_ids})
            except Exception:
                mutations = []
                for ci in range(0, len(sample_ids), 5000):
                    try:
                        chunk = fetch_api(
                            f'{BASE}/molecular-profiles/{mut_prof}/mutations/fetch',
                            data={'sampleIds': sample_ids[ci:ci+5000], 'entrezGeneIds': gene_ids})
                        mutations.extend(chunk)
                    except Exception:
                        pass

            if not mutations:
                print('  no mutations')
                continue

            s_idx = {s: i for i, s in enumerate(sample_ids)}
            g_idx = {g: i for i, g in enumerate(gene_ids)}
            M_gene = np.zeros((len(sample_ids), len(gene_ids)), dtype=bool)
            for mut in mutations:
                si2 = s_idx.get(mut.get('sampleId', ''))
                gi = g_idx.get(mut.get('entrezGeneId', 0))
                if si2 is not None and gi is not None:
                    M_gene[si2, gi] = True

            M_hm = np.zeros((len(sample_ids), n_h), dtype=bool)
            for i, gn in enumerate(gene_names_all):
                for hm in GENE_TO_HALLMARK.get(gn, []):
                    M_hm[:, hm] |= M_gene[:, i]

            # Survival
            os_status, os_months = {}, {}
            try:
                for c in fetch_api(f'{BASE}/studies/{sid}/clinical-data?clinicalDataType=PATIENT&attributeId=OS_STATUS'):
                    os_status[c['patientId']] = c['value']
                for c in fetch_api(f'{BASE}/studies/{sid}/clinical-data?clinicalDataType=PATIENT&attributeId=OS_MONTHS'):
                    try:
                        os_months[c['patientId']] = float(c['value'])
                    except (ValueError, KeyError):
                        pass
            except Exception:
                pass

            n_added = 0
            for p in range(len(sample_ids)):
                h = M_hm[p]
                if not h.any():
                    continue
                pid = s2p.get(sample_ids[p])
                key = f'{pid}_{sid}'
                if key in seen:
                    continue
                seen.add(key)

                h_list = h.tolist()
                os_mo = os_months.get(pid)
                os_st = os_status.get(pid, '')
                deceased = 1 if os_st in ('1:DECEASED', 'DECEASED') else 0

                patients.append({
                    'sig': ''.join(str(int(x)) for x in h),
                    'hallmarks': [int(x) for x in h_list],
                    'n_h': int(h.sum()),
                    'pairs': count_complete_pairs(h),
                    'n_walls': count_domain_walls(h),
                    'walls': compute_wall_features(h),
                    'wall_score': wall_risk_score(h),
                    'os_mo': os_mo,
                    'deceased': deceased,
                    'has_surv': os_mo is not None,
                    'study': sid,
                    'ctype': info['cancer_type'],
                    'o2': classify_o2(info['cancer_type']),
                })
                n_added += 1

            n_surv = sum(1 for pa in patients[-n_added:] if pa['has_surv']) if n_added else 0
            print(f'  {n_added:5d} pts, {n_surv:4d} surv')
        except Exception as e:
            print(f'  ERROR: {str(e)[:60]}')

        if (si + 1) % 20 == 0:
            print(f'  --- Running total: {len(patients):,} patients ---')
        sys.stdout.flush()

    return patients


