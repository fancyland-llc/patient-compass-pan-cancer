def run_analysis(patients):
    N = len(patients)
    print()
    print(BAR)
    print(f'  PATIENT COMPASS — REPRODUCIBILITY ANALYSIS (N = {N:,})')
    print(BAR)
    print()

    # ─── A: Dataset summary ───────────────────────────────────────
    n_surv = sum(1 for p in patients if p['has_surv'])
    n_deaths = sum(1 for p in patients if p['deceased'])
    sig_counter = Counter(p['sig'] for p in patients)

    print(f'  DATASET:')
    print(f'    Patients:      {N:>10,}')
    print(f'    With survival: {n_surv:>10,}')
    print(f'    Deaths:        {n_deaths:>10,}')
    print(f'    States:        {len(sig_counter):>10} / 256')
    print()

    # ─── B: Lopsidedness (§3.1) ───────────────────────────────────
    pair_counts = Counter(p['pairs'] for p in patients)
    print(f'  PAIR COMPLETENESS (§3.1):')
    for k in range(5):
        c = pair_counts.get(k, 0)
        pct = c / N * 100
        print(f'    {k} complete pairs: {c:>8,} ({pct:5.1f}%)')
    pct_zero = pair_counts.get(0, 0) / N * 100
    print(f'    >> {pct_zero:.1f}% have zero complete pairs (paper claims 85.8%)')
    print()

    # ─── C: Boltzmann fit (§3.4) ──────────────────────────────────
    ks = np.array([0, 1, 2, 3, 4], dtype=float)
    ns = np.array([pair_counts.get(k, 0) for k in range(5)], dtype=float)
    probs = ns / ns.sum()
    mask = probs > 0
    log_p = np.log(probs[mask])
    coeffs = np.polyfit(ks[mask], log_p, 1)
    beta = -coeffs[0]
    predicted = coeffs[1] + coeffs[0] * ks[mask]
    ss_res = np.sum((log_p - predicted)**2)
    ss_tot = np.sum((log_p - log_p.mean())**2)
    r2 = 1 - ss_res / ss_tot if ss_tot > 0 else 0

    print(f'  BOLTZMANN FIT (§3.4):')
    print(f'    P(k) ∝ exp(-β·k)')
    print(f'    β = {beta:.4f} (paper claims 1.6283)')
    print(f'    R² = {r2:.6f} (paper claims 0.9935)')
    print()

    # Transition ratios (cooperative effect)
    print(f'  COOPERATIVE EFFECT (§3.10):')
    for k in range(4):
        if ns[k] > 0 and ns[k+1] > 0:
            ratio = ns[k+1] / ns[k]
            print(f'    N({k+1})/N({k}) = {ratio:.4f}')
    print(f'    >> Ratios should INCREASE if pair completion is cooperative')
    print()

    # ─── D: Forbidden states (§3.2) ──────────────────────────────
    observed = set(sig_counter.keys())
    all_states = set(format(i, '08b') for i in range(256))
    forbidden = all_states - observed

    print(f'  STATE SPACE (§3.2):')
    print(f'    Observed:  {len(observed)}')
    print(f'    Forbidden: {len(forbidden)} (paper claims 94)')
    top_10 = sig_counter.most_common(10)
    top_10_pct = sum(c for _, c in top_10) / N * 100
    print(f'    Top 10 states cover: {top_10_pct:.1f}% (paper claims 70.0%)')
    print()

    # ─── E: Domain wall survival (§3.21) ──────────────────────────
    surv = [p for p in patients if p['has_surv'] and p['os_mo'] is not None and p['deceased']]

    if len(surv) > 100:
        print(f'  DOMAIN WALL SURVIVAL (§3.21):')
        wall_groups = {}
        for p in surv:
            w = p['n_walls']
            wall_groups.setdefault(w, []).append(p['os_mo'])

        for w in sorted(wall_groups.keys()):
            mos = wall_groups[w]
            print(f'    {w} broken pairs: N={len(mos):>6,}  median OS = {np.median(mos):6.1f} mo  mean OS = {np.mean(mos):6.1f} mo')

        # 0 walls vs 3+ walls
        lo_wall = np.array([p['os_mo'] for p in surv if p['n_walls'] <= 1])
        hi_wall = np.array([p['os_mo'] for p in surv if p['n_walls'] >= 3])
        if len(lo_wall) > 10 and len(hi_wall) > 10:
            z_w, p_w = mann_whitney_z(lo_wall, hi_wall)
            print(f'    LOW walls (0-1) vs HIGH walls (3+):')
            print(f'      z = {z_w:+.4f}, p = {p_w:.2e}')
            print(f'      >> Paper claims z = -19.34, p = 2.7e-83')
        print()

    # ─── F: Wall risk score survival ──────────────────────────────
    if len(surv) > 100:
        scores = np.array([p['wall_score'] for p in surv])
        months = np.array([p['os_mo'] for p in surv])
        med_score = np.median(scores)
        hi = scores >= med_score
        lo = ~hi

        z_ws, p_ws = mann_whitney_z(months[hi], months[lo])
        print(f'  WALL RISK SCORE SURVIVAL:')
        print(f'    High risk (score >= {med_score:.3f}): N={int(hi.sum()):,}, median OS = {np.median(months[hi]):.1f} mo')
        print(f'    Low risk  (score <  {med_score:.3f}): N={int(lo.sum()):,}, median OS = {np.median(months[lo]):.1f} mo')
        print(f'    Mann-Whitney z = {z_ws:+.4f}, p = {p_ws:.2e}')
        print()

    # ─── G: O₂-stratified survival (§3.24-3.26) ──────────────────
    if len(surv) > 100:
        print(f'  O₂-STRATIFIED SURVIVAL (§3.24-3.26):')
        for tier in ['HIGH', 'MEDIUM', 'LOW']:
            tier_pts = [p for p in surv if p['o2'] == tier]
            if len(tier_pts) < 20:
                print(f'    {tier:>6s} O₂: N={len(tier_pts):>5,} (too few)')
                continue
            mos = np.array([p['os_mo'] for p in tier_pts])
            genome_pts = [p for p in tier_pts if p['hallmarks'][6]]
            no_genome = [p for p in tier_pts if not p['hallmarks'][6]]
            if len(genome_pts) > 10 and len(no_genome) > 10:
                z_g, p_g = mann_whitney_z(
                    np.array([p['os_mo'] for p in genome_pts]),
                    np.array([p['os_mo'] for p in no_genome]))
                direction = 'protective' if z_g > 0 else 'harmful'
                print(f'    {tier:>6s} O₂: N={len(tier_pts):>5,}  Genome effect: z={z_g:+.3f} p={p_g:.2e} ({direction})')
            else:
                print(f'    {tier:>6s} O₂: N={len(tier_pts):>5,}  (insufficient Genome+ patients)')
        print(f'    >> Paper claims Genome is harmful in HIGH O₂, protective in LOW O₂')
        print()

    # ─── H: Per-study survival + Stouffer meta-analysis (§3.6) ────
    study_ids = set(p['study'] for p in patients)
    study_z_results = []

    print(f'  PER-STUDY DOMAIN WALL SURVIVAL:')
    print(f'    {"Study":<40s}  {"N":>5s}  {"z":>8s}  {"p":>10s}  {"Δ OS":>8s}')
    print('    ' + '-' * 80)

    for sid in sorted(study_ids):
        s_surv = [p for p in patients if p['study'] == sid and p['has_surv']
                  and p['os_mo'] is not None and p['deceased']]
        if len(s_surv) < 30:
            continue
        lo_wall = np.array([p['os_mo'] for p in s_surv if p['n_walls'] <= 1])
        hi_wall = np.array([p['os_mo'] for p in s_surv if p['n_walls'] >= 2])
        if len(lo_wall) < 5 or len(hi_wall) < 5:
            continue
        z_s, p_s = mann_whitney_z(lo_wall, hi_wall)
        delta = float(np.median(lo_wall) - np.median(hi_wall))
        sig_mark = '**' if p_s < 0.05 else '*' if p_s < 0.10 else ''
        label = sid[:40]
        print(f'    {label:<40s}  {len(s_surv):5d}  {z_s:+8.3f}  {p_s:10.2e}  {delta:+7.1f}mo  {sig_mark}')
        study_z_results.append({'study': sid, 'n': len(s_surv), 'z': z_s, 'p': p_s})

    print()
    if study_z_results:
        zs = np.array([r['z'] for r in study_z_results])
        ns_arr = np.array([r['n'] for r in study_z_results])
        n_tested = len(study_z_results)
        n_sig = sum(1 for r in study_z_results if r['p'] < 0.05)

        # Stouffer unweighted
        z_stouf = zs.sum() / sqrt(n_tested)
        p_stouf = erfc(abs(z_stouf) / sqrt(2))
        # Stouffer weighted
        w = np.sqrt(ns_arr)
        z_wt = (w * zs).sum() / sqrt((w**2).sum())
        p_wt = erfc(abs(z_wt) / sqrt(2))
        # Direction-agnostic (|z|)
        z_abs = np.abs(zs).sum() / sqrt(n_tested)
        p_abs = erfc(abs(z_abs) / sqrt(2))

        print(f'  META-ANALYSIS — Stouffer (§3.6):')
        print(f'    Studies tested:  {n_tested}')
        print(f'    Significant p<0.05: {n_sig} ({n_sig/n_tested*100:.0f}%)')
        print(f'    Unweighted:   z = {z_stouf:+.3f}, p = {p_stouf:.2e}')
        print(f'    Weighted:     z = {z_wt:+.3f}, p = {p_wt:.2e}')
        print(f'    |z| (direction-agnostic): z = {z_abs:+.3f}, p = {p_abs:.2e}')
        print(f'    >> Paper claims Z_|z| = +15.29, p = 8.9e-53')
        print()

    # ─── I: Pair-specific survival (§3.9) ─────────────────────────
    print(f'  PAIR-SPECIFIC SURVIVAL (§3.9):')
    for pair_idx, (h_a, h_b) in enumerate(PAIRS):
        name = f'{SHORT[h_a]}+{SHORT[h_b]}'
        comp = [p for p in surv if p['hallmarks'][h_a] and p['hallmarks'][h_b]]
        inc = [p for p in surv if not (p['hallmarks'][h_a] and p['hallmarks'][h_b])]
        if len(comp) > 30 and len(inc) > 30:
            z_p, p_p = mann_whitney_z(
                np.array([p['os_mo'] for p in comp]),
                np.array([p['os_mo'] for p in inc]))
            delta = float(np.median([p['os_mo'] for p in comp]) - np.median([p['os_mo'] for p in inc]))
            print(f'    Pair {pair_idx+1} ({name:15s}): N_comp={len(comp):>6,} N_inc={len(inc):>6,}  z={z_p:+.3f} p={p_p:.2e} Δ={delta:+.1f}mo')
    print()

    # ─── J: Frustrated Proliferator (§3.23) ───────────────────────
    if len(surv) > 100:
        frust = [p for p in surv if p['hallmarks'][0] and not p['hallmarks'][4]]  # Prolif_only
        non_frust = [p for p in surv if not (p['hallmarks'][0] and not p['hallmarks'][4])]
        if len(frust) > 20 and len(non_frust) > 20:
            z_f, p_f = mann_whitney_z(
                np.array([p['os_mo'] for p in frust]),
                np.array([p['os_mo'] for p in non_frust]))
            hr = exp(WALL_COEFS['Prolif_only'])
            print(f'  FRUSTRATED PROLIFERATOR (§3.23):')
            print(f'    Prolif only: N={len(frust):,}, median OS={np.median([p["os_mo"] for p in frust]):.1f}mo')
            print(f'    Others:      N={len(non_frust):,}, median OS={np.median([p["os_mo"] for p in non_frust]):.1f}mo')
            print(f'    z = {z_f:+.4f}, p = {p_f:.2e}')
            print(f'    Cox HR = {hr:.2f} (paper claims HR=1.81 in LOW O₂)')
            print()

    # ─── K: Forbidden state topology ──────────────────────────────
    print(f'  FORBIDDEN STATE TOPOLOGY (§3.2, §3.5):')
    for cp in range(5):
        poss = [s for s in all_states if count_complete_pairs([int(c) for c in s]) == cp]
        obs = [s for s in poss if s in observed]
        forb = [s for s in poss if s not in observed]
        pct = len(forb) / len(poss) * 100 if poss else 0
        print(f'    {cp} pairs: {len(obs):>3} observed, {len(forb):>3} forbidden ({pct:.0f}% forbidden)')
    print()

    # ─── L: Cox PH model — re-derive wall coefficients from scratch ─
    # Model: 10 wall features + 2 O₂ indicators = 12 covariates
    # lifelines CoxPHFitter(penalizer=0.01), Efron ties, O₂-tier patients only
    cox_surv = [p for p in patients if p['has_surv'] and p['os_mo'] is not None
                and p['os_mo'] > 0 and p['deceased'] is not None
                and TISSUE_O2.get(p.get('ctype', '')) is not None]
    if len(cox_surv) > 500:
        print(f'  COX PH MODEL — RE-DERIVING WALL COEFFICIENTS (§3.23):')
        print(f'    Fitting Cox PH (lifelines, Efron ties, penalizer=0.01) on {len(cox_surv):,} patients...')
        print(f'    (12 covariates: 10 walls + O₂_high + O₂_low)')

        # 10 wall features + O2_high + O2_low
        wall_names = list(WALL_COEFS.keys())
        feature_names = wall_names + ['O2_high', 'O2_low']
        X_cox = np.array([
            [p['walls'].get(f, 0) for f in wall_names]
            + [1 if TISSUE_O2[p['ctype']] == 'HIGH' else 0,
               1 if TISSUE_O2[p['ctype']] == 'LOW' else 0]
            for p in cox_surv
        ], dtype=float)
        t_cox = np.array([p['os_mo'] for p in cox_surv], dtype=float)
        e_cox = np.array([p['deceased'] for p in cox_surv], dtype=float)

        params, se_dict, c_index, converged = cox_fit_lifelines(
            X_cox, t_cox, e_cox, feature_names)

        status = 'CONVERGED' if converged else 'DID NOT CONVERGE'
        print(f'    Status: {status}, C-index: {c_index:.4f}')
        print(f'    {"Feature":<20s}  {"Paper β":>10s}  {"Fitted β":>10s}  {"SE":>8s}  {"HR":>6s}  {"Match?":>8s}')
        print('    ' + '-' * 70)

        all_close = True
        for feat in wall_names:
            paper_b = WALL_COEFS[feat]
            fit_b = params[feat]
            se = se_dict[feat]
            hr = exp(fit_b)
            close = abs(fit_b - paper_b) < max(2 * se, 0.01)
            mark = '✓' if close else '✗'
            if not close:
                all_close = False
            print(f'    {feat:<20s}  {paper_b:+10.6f}  {fit_b:+10.6f}  {se:8.4f}  {hr:6.3f}  {mark:>8s}')

        print()
        if all_close:
            print(f'    >> All coefficients independently re-derived within tolerance.')
        else:
            print(f'    >> Some coefficients differ — see SE column for confidence intervals.')
            print(f'    >> (Differences may reflect penalization or subsample effects.)')
        print()

    # ─── Summary ──────────────────────────────────────────────────
    print(BAR)
    print('  REPRODUCIBILITY SUMMARY')
    print(BAR)
    print()
    checks = [
        ('Patients ≥ 100,000', N >= 100000),
        ('States ≈ 162', 155 <= len(observed) <= 170),
        ('Forbidden ≈ 94', 86 <= len(forbidden) <= 101),
        ('Lopsidedness > 80%', pct_zero > 80),
        ('β ≈ 1.63', abs(beta - 1.63) < 0.15),
        ('R² > 0.99', r2 > 0.99),
        ('Top 10 > 60%', top_10_pct > 60),
    ]
    for label, ok in checks:
        status = '✓ PASS' if ok else '✗ FAIL'
        print(f'    {status}  {label}')
    print()


